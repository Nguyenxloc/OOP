/// use key " this " to return statement
public class Main { 
  int x; 
 
  // Constructor with a parameter
  public Main(int x) { 
    this.x = 1; 
  } 

  // Call the constructor
  public static void main(String[] args)  { 
     
    System.out.println("Value of x = " + x);
  } 
} 

